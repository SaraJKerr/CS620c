/** The integerproduct application assigns numbers to three variables 
 * and multiplies them.
 */
 class integerproduct
 {
	public static void main(String[] args)
	{
		int numberX = 10;
		int numberY = 43;
		int numberZ = 15;
		int result = numberX * numberY * numberZ;
		System.out.println("The result is" + " " + result);
	}
}