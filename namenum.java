/** The namenum application hard codes information to two string variables and
 * concatenates them, printing the result to the screen.
 */
 class namenum
 {
	public static void main(String[] args)
	{
		String name = "My name is Sara";
		String num = "I haven't got a student number yet.";
		System.out.println(name + " and " + num);
	}
}