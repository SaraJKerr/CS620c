/** The integer application assigns numbers to three variables and prints
 * them sequentially.
 */
 class integer
 {
	public static void main(String[] args)
	{
		int numberX = 10;
		int numberY = 43;
		int numberZ = 15;
		System.out.println(numberX);
		System.out.println(numberY);
		System.out.println(numberZ);
	}
}