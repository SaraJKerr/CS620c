/**
 * The HelloWorldString class implements an application that
 * prints a statement starting "Hello World!" to standard output.
 * However, this time a String variable is created using = to 
 * assign the content to helloStr
 */
 class HelloWorldString
 {
	public static void main(String[] args)
	{
		//System.out.println("Hello World!");// Display the string.
		String helloStr = "Hello World! This is a string variable";
		System.out.println(helloStr);
	}
}